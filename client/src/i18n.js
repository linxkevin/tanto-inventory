export const VENDOR_COLORS = [
  ['#D85A30','#FAECE7','#4A1B0C'],
  ['#378ADD','#E6F1FB','#042C53'],
  ['#1D9E75','#E1F5EE','#085041'],
  ['#BA7517','#FAEEDA','#633806'],
  ['#534AB7','#EEEDFE','#26215C'],
  ['#993556','#FBEAF0','#4B1528'],
];
export const SERVER_VENDOR = 'サーバー棚卸し';
export const SERVER_COLOR  = ['#534AB7','#EEEDFE','#3C3489'];
export const KITCHEN_VENDORS = ['JFC','Cherry','Wismettac','Kukui','Sun Noodle','Seven'];
export const ALL_VENDORS = [...KITCHEN_VENDORS, SERVER_VENDOR];

export function vcolor(vendor, idx = 0) {
  if (vendor === SERVER_VENDOR) return SERVER_COLOR[idx];
  const i = KITCHEN_VENDORS.indexOf(vendor) % VENDOR_COLORS.length;
  return VENDOR_COLORS[Math.max(0,i)][idx];
}

export function itemName(item, lang) {
  if (lang === 'en') return item.name_en || item.name_ja;
  if (lang === 'zh') return item.name_zh || item.name_ja;
  return item.name_ja;
}
